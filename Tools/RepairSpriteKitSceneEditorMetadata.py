#!/usr/bin/env python3
"""
Repairs SpriteKit .sks archives generated by NSKeyedArchiver so Xcode can open
them in the SpriteKit scene editor instead of only offering Quick Look / Hex.

The generator archives the concrete scene subclass as the root object, which is
runtime-loadable but not editor-friendly. Xcode-authored .sks files instead use
an SKScene root plus preview metadata that points back to the custom class.
"""

from __future__ import annotations

import argparse
import plistlib
import re
import sys
from pathlib import Path
from plistlib import UID
from typing import Any


PREVIEW_CUSTOM_CLASS_KEY = "_PB_previewSKNodeCustomClassName"
PREVIEW_WIDTH_KEY = "_PB_previewSKEditorSceneSizeWidth"
PREVIEW_HEIGHT_KEY = "_PB_previewSKEditorSceneSizeHeight"
PREVIEW_SHADER_UNIFORMS_KEY = "_PB_previewSpriteShaderUniforms"
PREVIEW_UNIQUE_ID_KEY = "_PB_previewSKNodeUniqueID"

CUSTOM_SCENE_CLASS_NAMES = {
    "GameScene",
    "ForestScene",
    "BigOakTreeScene",
    "CaveScene",
    "HouseScene",
    "TitleScene",
}

CUSTOM_NODE_BASE_CLASSES = {
    "IngredientStation": ("SKSpriteNode", ["SKSpriteNode", "SKNode", "NSResponder", "NSObject"]),
    "RotatableObject": ("SKSpriteNode", ["SKSpriteNode", "SKNode", "NSResponder", "NSObject"]),
    "SaveSystemButton": ("SKSpriteNode", ["SKSpriteNode", "SKNode", "NSResponder", "NSObject"]),
    "PowerBreaker": ("SKSpriteNode", ["SKSpriteNode", "SKNode", "NSResponder", "NSObject"]),
    "Window": ("SKSpriteNode", ["SKSpriteNode", "SKNode", "NSResponder", "NSObject"]),
    "StorageContainer": ("SKSpriteNode", ["SKSpriteNode", "SKNode", "NSResponder", "NSObject"]),
    "DrinkCreator": ("SKNode", ["SKNode", "NSResponder", "NSObject"]),
}


class SceneArchive:
    def __init__(self, path: Path):
        self.path = path
        with path.open("rb") as handle:
            self.data = plistlib.load(handle)
        self.objects = self.data["$objects"]
        self.root = self.objects[self.data["$top"]["root"].data]

    def deref(self, value: Any) -> Any:
        if isinstance(value, UID):
            return self.objects[value.data]
        return value

    def append(self, value: Any) -> UID:
        self.objects.append(value)
        return UID(len(self.objects) - 1)

    def find_class_uid(self, class_name: str) -> UID | None:
        for index, obj in enumerate(self.objects):
            if isinstance(obj, dict) and obj.get("$classname") == class_name:
                return UID(index)
        return None

    def append_class_uid(self, class_name: str, classes: list[str]) -> UID:
        existing = self.find_class_uid(class_name)
        if existing is not None:
            return existing
        return self.append({"$classname": class_name, "$classes": classes})

    def find_literal_uid(self, value: Any) -> UID | None:
        for index, obj in enumerate(self.objects):
            if type(obj) is type(value) and obj == value:
                return UID(index)
        return None

    def literal_uid(self, value: Any) -> UID:
        existing = self.find_literal_uid(value)
        if existing is not None:
            return existing
        return self.append(value)

    def scene_rect(self) -> tuple[float, float]:
        bounds = self.deref(self.root["Scene_bounds"])
        rect = self.deref(bounds["NS.rectval"])
        match = re.match(r"\{\{[^,]+, [^}]+\}, \{([^,]+), ([^}]+)\}\}", rect)
        if not match:
            raise ValueError(f"Could not parse scene bounds {rect!r} in {self.path}")
        return float(match.group(1)), float(match.group(2))

    def preview_info(self) -> dict[str, Any] | None:
        info = self.deref(self.root["_info"])
        if not isinstance(info, dict):
            return None

        keys = [self.deref(item) for item in info.get("NS.keys", [])]
        objects = [self.deref(item) for item in info.get("NS.objects", [])]
        return dict(zip(keys, objects))

    def custom_scene_name(self) -> str:
        info = self.preview_info() or {}
        if PREVIEW_CUSTOM_CLASS_KEY in info and isinstance(info[PREVIEW_CUSTOM_CLASS_KEY], str):
            return info[PREVIEW_CUSTOM_CLASS_KEY]

        root_class = self.deref(self.root["$class"])
        class_name = root_class.get("$classname")
        if isinstance(class_name, str) and class_name != "SKScene":
            return class_name

        return self.path.stem

    def is_editor_friendly(self) -> bool:
        root_class = self.deref(self.root["$class"])
        info = self.preview_info() or {}
        return (
            isinstance(root_class, dict)
            and root_class.get("$classname") == "SKScene"
            and PREVIEW_CUSTOM_CLASS_KEY in info
        )

    def repair(self) -> bool:
        changed_root = self.repair_scene_root()
        changed_nodes = self.repair_custom_nodes()
        return changed_root or changed_nodes

    def repair_scene_root(self) -> bool:
        width, height = self.scene_rect()
        custom_scene_name = self.custom_scene_name()
        should_be_custom_scene = custom_scene_name in CUSTOM_SCENE_CLASS_NAMES
        if not should_be_custom_scene:
            return False

        root_class = self.deref(self.root["$class"])
        preview_info = self.preview_info() or {}
        expected_info_keys = {
            PREVIEW_CUSTOM_CLASS_KEY,
            PREVIEW_WIDTH_KEY,
            PREVIEW_SHADER_UNIFORMS_KEY,
            PREVIEW_HEIGHT_KEY,
        }

        is_already_fixed = (
            isinstance(root_class, dict)
            and root_class.get("$classname") == "SKScene"
            and preview_info.get(PREVIEW_CUSTOM_CLASS_KEY) == custom_scene_name
            and PREVIEW_WIDTH_KEY in preview_info
            and PREVIEW_HEIGHT_KEY in preview_info
            and set(preview_info.keys()) == expected_info_keys
        )
        if is_already_fixed and should_be_custom_scene:
            return False

        ns_array_class = self.append_class_uid("NSArray", ["NSArray", "NSObject"])
        ns_mutable_dictionary_class = self.append_class_uid(
            "NSMutableDictionary",
            ["NSMutableDictionary", "NSDictionary", "NSObject"],
        )
        sk_scene_class = self.append_class_uid(
            "SKScene",
            ["SKScene", "SKEffectNode", "SKNode", "NSResponder", "NSObject"],
        )

        custom_class_key_uid = self.literal_uid(PREVIEW_CUSTOM_CLASS_KEY)
        preview_width_key_uid = self.literal_uid(PREVIEW_WIDTH_KEY)
        shader_uniforms_key_uid = self.literal_uid(PREVIEW_SHADER_UNIFORMS_KEY)
        preview_height_key_uid = self.literal_uid(PREVIEW_HEIGHT_KEY)

        custom_scene_uid = self.literal_uid(custom_scene_name)
        root_original_class_uid = self.literal_uid("SKScene")
        preview_width_uid = self.append(width)
        preview_height_uid = self.append(height)
        empty_array_uid = self.append({"NS.objects": [], "$class": ns_array_class})

        preview_info_uid = self.append(
            {
                "NS.keys": [
                    custom_class_key_uid,
                    preview_width_key_uid,
                    shader_uniforms_key_uid,
                    preview_height_key_uid,
                ],
                "NS.objects": [
                    custom_scene_uid,
                    preview_width_uid,
                    empty_array_uid,
                    preview_height_uid,
                ],
                "$class": ns_mutable_dictionary_class,
            }
        )

        self.root["_info"] = preview_info_uid
        self.root["_originalClass"] = root_original_class_uid
        self.root["$class"] = sk_scene_class
        return True

    def repair_custom_nodes(self) -> bool:
        custom_class_key_uid = self.literal_uid(PREVIEW_CUSTOM_CLASS_KEY)
        preview_unique_id_key_uid = self.literal_uid(PREVIEW_UNIQUE_ID_KEY)
        ns_mutable_dictionary_class = self.append_class_uid(
            "NSMutableDictionary",
            ["NSMutableDictionary", "NSDictionary", "NSObject"],
        )

        unique_id = 1000
        changed = False

        for obj in self.objects:
            if not isinstance(obj, dict):
                continue

            class_uid = obj.get("$class")
            if not isinstance(class_uid, UID):
                continue

            class_info = self.deref(class_uid)
            if not isinstance(class_info, dict):
                continue

            custom_class_name = class_info.get("$classname")
            if custom_class_name not in CUSTOM_NODE_BASE_CLASSES:
                continue

            base_class_name, base_class_hierarchy = CUSTOM_NODE_BASE_CLASSES[custom_class_name]
            obj["$class"] = self.append_class_uid(base_class_name, base_class_hierarchy)
            obj["_originalClass"] = self.literal_uid(base_class_name)

            info_uid = obj.get("_info")
            if not isinstance(info_uid, UID) or info_uid.data == 0:
                info_uid = self.append(
                    {
                        "NS.keys": [],
                        "NS.objects": [],
                        "$class": ns_mutable_dictionary_class,
                    }
                )
                obj["_info"] = info_uid

            info = self.deref(info_uid)
            keys = [self.deref(key) for key in info.get("NS.keys", [])]
            objects = list(info.get("NS.objects", []))

            def set_info_value(key_uid: UID, value_uid: UID) -> None:
                key = self.deref(key_uid)
                if key in keys:
                    objects[keys.index(key)] = value_uid
                else:
                    info.setdefault("NS.keys", []).append(key_uid)
                    objects.append(value_uid)
                    keys.append(key)

            set_info_value(custom_class_key_uid, self.literal_uid(custom_class_name))
            set_info_value(preview_unique_id_key_uid, self.literal_uid(unique_id))
            unique_id += 1

            info["NS.objects"] = objects
            changed = True

        return changed

    @staticmethod
    def format_number(value: float) -> str:
        rounded = round(value)
        if abs(value - rounded) < 0.0001:
            return str(int(rounded))
        return f"{value:.3f}".rstrip("0").rstrip(".")

    def save(self) -> None:
        with self.path.open("wb") as handle:
            plistlib.dump(self.data, handle, fmt=plistlib.FMT_BINARY)


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("paths", nargs="+", help="SpriteKit .sks files to repair in place")
    args = parser.parse_args(argv)

    for raw_path in args.paths:
        path = Path(raw_path)
        archive = SceneArchive(path)
        changed = archive.repair()
        if changed:
            archive.save()
            print(f"repaired {path}")
        else:
            print(f"already editor-friendly {path}")

    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
